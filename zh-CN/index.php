        var $ = mdui.$;
        function copyToClipboard(s) {
            mdui.snackbar({
                message: '文本已复制',
                position: 'right-top',
            });
            if (window.clipboardData) {
                window.clipboardData.setData('text', s);
            } else {
                (function (s) {
                    document.oncopy = function (e) {
                        e.clipboardData.setData('text', s);
                        e.preventDefault();
                        document.oncopy = null;
                    }
                })(s);
                document.execCommand('Copy');
            }
        }
